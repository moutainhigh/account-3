DROP TABLE IF EXISTS ACCOUNTINFO;

CREATE TABLE ACCOUNTINFO 
(
	AIF_ACCOUNTNO			VARCHAR(20)		NOT NULL,	/*账号*/
	AIF_ACCOUNTNAME			VARCHAR(60)		NOT NULL,	/*户名*/
	AIF_USERNO			VARCHAR(30)			,	/*用户号*/
	AIF_BALANCE			DECIMAL(18,2)		NOT NULL,	/*余额*/
	AIF_BALANCEFROZEN		DECIMAL(18,2)		NOT NULL,	/*冻结余额*/
	AIF_STATUS			VARCHAR(2)		NOT NULL,	/*状态*/
	AIF_OPENTIME			CHAR(14)		NOT NULL,	/*开户时间*/
	AIF_CHANNELSYSTEMNO	VARCHAR(30)	,	/*渠道系统号*/
	AIF_CHANNELSERIALNO	VARCHAR(30)	,	/*渠道流水号*/
	PRIMARY KEY (AIF_ACCOUNTNO)
)DEFAULT CHARSET=utf8;

